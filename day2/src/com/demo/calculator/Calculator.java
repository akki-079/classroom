package com.demo.calculator;

public class Calculator {
	/**
	 * 
	 * @param a input 1 for add method
	 * @param b input 2 for add method
	 * @return a+b
	 */

	public static int add(int a, int b) {
		return a+b;
	}
	/**
	 * 
	 * @param a input 1 for multiply method
	 * @param b input 2 for multiply method
	 * @return a*b
	 */
	public static int multiply(int a, int b) {
		return a*b;
	}

}
